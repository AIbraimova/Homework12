public class Person {
    private String nameOfPerson;
    private byte ageOfPerson;
    private String hobbyOfPerson;

    public String getNameOfPerson() {
        return nameOfPerson;
    }

    public void setNameOfPerson(String nameOfPerson) {
        this.nameOfPerson = nameOfPerson;
    }

    public byte getAgeOfPerson() {
        return ageOfPerson;
    }

    public void setAgeOfPerson(byte ageOfPerson) {
        this.ageOfPerson = ageOfPerson;
    }

    public String getHobbyOfPerson() {
        return hobbyOfPerson;
    }

    public void setHobbyOfPerson(String hobbyOfPerson) {
        this.hobbyOfPerson = hobbyOfPerson;
    }
}
