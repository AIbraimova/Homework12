public class University {
    private String nameOfUniversity;
    private String adressOfUniversity;
    private String rectorOfUniversity;

    public String getNameOfUniversity() {
        return nameOfUniversity;
    }

    public void setNameOfUniversity(String nameOfUniversity) {
        this.nameOfUniversity = nameOfUniversity;
    }

    public String getAdressOfUniversity() {
        return adressOfUniversity;
    }

    public void setAdressOfUniversity(String adressOfUniversity) {
        this.adressOfUniversity = adressOfUniversity;
    }

    public String getRectorOfUniversity() {
        return rectorOfUniversity;
    }

    public void setRectorOfUniversity(String rectorOfUniversity) {
        this.rectorOfUniversity = rectorOfUniversity;
    }
}
